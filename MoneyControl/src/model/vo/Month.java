package model.vo;

public class Month {
	
	private int idMonth;
	private String name;
	
	public int getIdMonth() {
		return idMonth;
	}
	
	public void setIdMonth(int idMonth) {
		this.idMonth = idMonth;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}	
}
